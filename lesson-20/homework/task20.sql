select distinct s1.customername
from #sales s1
where exists (
    select 1 from #sales s2
    where s2.customername = s1.customername
      and month(s2.saledate) = 3
      and year(s2.saledate) = 2024
)


select top 1 product, sum(quantity * price) as totalrevenue
from #sales
group by product
order by totalrevenue desc
with saleamounts as (
    select (quantity * price) as amount
    from #sales
)
select max(amount) as secondhighest
from saleamounts
where amount < (select max(amount) from saleamounts)

select salemonth, sum(quantity) as totalquantity
from (
    select quantity, month(saledate) as salemonth
    from #sales
) a
group by salemonth



